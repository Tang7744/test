class WaterfallLayout {
    constructor() {
        this.container = document.querySelector('.waterfall');
        this.items = Array.from(document.querySelectorAll('.waterfall-item'));
        this.gap = 20; // 间隙
        this.minCardWidth = 260; // 最小卡片宽度

        // 初始设置卡片固定宽度，防止布局抖动
        this.items.forEach(item => {
            item.style.width = `${this.minCardWidth}px`;
        });

        this.init();
        this.bindEvents();
        this.loadingQueue = Promise.resolve(); // 添加加载队列
        this.initModal(); // 初始化模态框
    }

    init() {
        // 先进行初始布局
        this.layout();
        // 然后再加载图片
        this.loadImages();
    }

    async loadImages() {
        const images = Array.from(document.querySelectorAll('.item-image img:not([src])'));

        // 先设置固定高度，防止加载中的抖动
        images.forEach(img => {
            const item = img.closest('.item-image');
            if (item) {
                item.style.height = '260px'; // 设置固定高度
            }
        });

        // 使用 Promise.all 并行加载图片
        const loadPromises = images.map(async (img) => {
            try {
                const type = img.alt;
                const url = await getRandomImageUrl(type);
                if (url) {
                    // 创建新的Image对象进行预加载
                    return new Promise((resolve) => {
                        const tempImg = new Image();
                        tempImg.onload = () => {
                            img.src = url;
                            this.handleImageLoad(img);
                            resolve();
                        };
                        tempImg.onerror = () => {
                            this.handleImageError(img);
                            resolve();
                        };
                        tempImg.src = url;
                    });
                }
            } catch (error) {
                console.error('加载图片失败:', error);
            }
        });

        await Promise.all(loadPromises);
        // 所有图片加载完成后再一次性重新布局
        this.layout();
    }

    bindEvents() {
        // 监听窗口大小变化
        let resizeTimer;
        window.addEventListener('resize', () => {
            clearTimeout(resizeTimer);
            resizeTimer = setTimeout(() => this.layout(), 100);
        });

        // 监听卡片点击
        this.container.addEventListener('click', (e) => {
            const item = e.target.closest('.waterfall-item');
            if (item) {
                this.showDetail(item);
            }
        });
    }

    handleImageLoad(img) {
        const item = img.closest('.waterfall-item');
        if (item) {
            item.classList.remove('loading');
        }
    }

    handleImageError(img) {
        const item = img.closest('.waterfall-item');
        if (item) {
            item.classList.remove('loading');
            const type = img.alt;
            getRandomImageUrl(type).then(url => {
                if (url) {
                    img.src = url;
                }
            });
        }
    }

    getColumns() {
        const containerWidth = this.container.clientWidth;
        return Math.max(2, Math.floor(containerWidth / (this.minCardWidth + this.gap)));
    }

    layout() {
        this.items = Array.from(document.querySelectorAll('.waterfall-item'));

        // 计算列数
        const columns = Math.max(2, this.getColumns());

        // 计算固定的列宽
        const itemWidth = Math.floor((this.container.clientWidth - (this.gap * (columns - 1))) / columns);

        // 初始化每列的高度为0
        const columnHeights = new Array(columns).fill(0);

        // 为每个项目设置固定宽度并放置到最短的列中
        this.items.forEach(item => {
            item.style.width = `${itemWidth}px`;

            const minHeight = Math.min(...columnHeights);
            const column = columnHeights.indexOf(minHeight);

            const left = column * (itemWidth + this.gap);
            const top = minHeight;

            item.style.transform = `translate(${left}px, ${top}px)`;

            columnHeights[column] = top + item.offsetHeight + this.gap;
        });

        this.container.style.height = `${Math.max(...columnHeights)}px`;
    }

    initModal() {
        const modal = document.querySelector('.modal');
        const closeBtn = modal.querySelector('.modal-close');

        closeBtn.addEventListener('click', () => {
            modal.classList.remove('active');
            document.body.style.overflow = 'auto';
        });

        modal.addEventListener('click', (e) => {
            if (e.target === modal) {
                modal.classList.remove('active');
                document.body.style.overflow = 'auto';
            }
        });

        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape' && modal.classList.contains('active')) {
                modal.classList.remove('active');
                document.body.style.overflow = 'auto';
            }
        });
    }

    showDetail(item) {
        const modal = document.querySelector('.modal');
        const modalImg = modal.querySelector('.modal-image img');
        const modalAvatar = modal.querySelector('.modal-avatar');
        const modalUsername = modal.querySelector('.modal-username');
        const modalTitle = modal.querySelector('.modal-title');
        const modalDescription = modal.querySelector('.modal-description');
        const modalTags = modal.querySelector('.modal-tags');
        const modalCommentsSection = modal.querySelector('.modal-comments-section');
        const modalCommentsCount = modal.querySelector('.comments-count');
        const modalCommentList = modal.querySelector('.comment-list');
        const modalLikes = modal.querySelector('.modal-likes');
        const heartIcon = modal.querySelector('.icon-heart');
        const starIcon = modal.querySelector('.icon-star');

        // 获取卡片ID（需要先在HTML中为每个卡片添加data-id属性）
        const cardId = item.getAttribute('data-id') || '1';
        const data = cardData[cardId] || cardData[1]; // 默认使用第一个卡片数据

        // 设置模态框数据
        modalImg.src = item.querySelector('.item-image img').src;
        modalImg.alt = item.querySelector('.item-image img').alt;

        // 用户信息
        const avatar = item.querySelector('.avatar');
        modalAvatar.textContent = data.username.charAt(0);
        modalAvatar.style.backgroundColor = avatar.style.backgroundColor || getRandomColor();
        modalUsername.textContent = data.username;
        modal.querySelector('.modal-time').textContent = data.time;

        // 内容信息
        modalTitle.textContent = data.title;
        modalDescription.innerHTML = data.description;
        modalLikes.textContent = data.likes;
        modalCommentsCount.textContent = data.comments;

        // 生成标签
        modalTags.innerHTML = '';
        data.tags.forEach(tag => {
            const tagElement = document.createElement('span');
            tagElement.className = 'tag';
            tagElement.textContent = tag;
            modalTags.appendChild(tagElement);
        });

        // 生成评论
        modalCommentList.innerHTML = '';
        data.commentsData.forEach(comment => {
            const commentItem = document.createElement('div');
            commentItem.className = 'comment-item';
            commentItem.innerHTML = `
                <div class="comment-avatar" style="background-color: ${comment.avatarColor}">
                    ${comment.user.charAt(0)}
                </div>
                <div class="comment-content">
                    <div class="comment-user">${comment.user}</div>
                    <div class="comment-text">${comment.text}</div>
                    <div class="comment-footer">
                        <span class="comment-time">${comment.time}</span>
                        <span class="comment-like">❤️ ${comment.likes}</span>
                    </div>
                </div>
            `;
            modalCommentList.appendChild(commentItem);
        });

        // 检查是否已经点赞（从localStorage获取）
        const likeKey = `like_${modalImg.src}`;
        const isLiked = localStorage.getItem(likeKey) === 'true';

        // 设置点赞状态
        if (isLiked) {
            heartIcon.classList.add('active');
        } else {
            heartIcon.classList.remove('active');
        }

        // 检查是否已收藏
        const starKey = `star_${modalImg.src}`;
        const isStarred = localStorage.getItem(starKey) === 'true';

        // 设置收藏状态
        if (isStarred) {
            starIcon.classList.add('active');
        } else {
            starIcon.classList.remove('active');
        }

        // 显示模态框
        modal.classList.add('active');
        document.body.style.overflow = 'hidden';

        // 移除旧的事件监听器（防止重复添加）
        const heartContainer = modal.querySelector('.modal-stats span:first-child');
        const newHeartContainer = heartContainer.cloneNode(true);
        heartContainer.parentNode.replaceChild(newHeartContainer, heartContainer);

        const starContainer = modal.querySelector('.modal-stats span:nth-child(3)');
        const newStarContainer = starContainer.cloneNode(true);
        starContainer.parentNode.replaceChild(newStarContainer, starContainer);

        // 添加新的点赞事件监听
        newHeartContainer.addEventListener('click', function() {
            const icon = this.querySelector('.icon-heart');
            const count = this.querySelector('.modal-likes');

            // 切换点赞状态
            icon.classList.toggle('active');

            // 添加点赞动画
            icon.style.transform = 'scale(1.3)';
            setTimeout(() => {
                icon.style.transform = 'scale(1)';
            }, 200);

            // 更新点赞数和本地存储
            const isNowLiked = icon.classList.contains('active');
            count.textContent = isNowLiked ? (parseInt(data.likes) + 1) : data.likes;
            localStorage.setItem(likeKey, isNowLiked);

            // 同时更新瀑布流中对应的卡片
            const likeIcon = item.querySelector('.like-icon');
            const likeCount = likeIcon.nextElementSibling;
            if (isNowLiked) {
                likeIcon.classList.add('active');
                likeCount.textContent = parseInt(data.likes) + 1;
            } else {
                likeIcon.classList.remove('active');
                likeCount.textContent = data.likes;
            }
        });

        // 添加新的收藏事件监听
        newStarContainer.addEventListener('click', function() {
            const icon = this.querySelector('.icon-star');

            // 切换收藏状态
            icon.classList.toggle('active');

            // 添加收藏动画
            icon.style.transform = 'scale(1.3)';
            setTimeout(() => {
                icon.style.transform = 'scale(1)';
            }, 200);

            // 更新本地存储
            localStorage.setItem(starKey, icon.classList.contains('active'));
        });
    }
}

